
$(document).ready(function() {
  let visualization = {
    "Class to Video": {"intro": `<b> Video generation samples </b> on UCF-101 dataset. Each row shows sampled frames from a 16-frame, 128 × 128 resolution
            sequence generated by NAR-XL across various action categories.`, "visua": `<div class="column is-full-width c2v">
        <img src="./static/images/c2v_ucf101.png"
                 alt="C2V visualization."/>
      </div>`},
    "Class to Image": {"intro": `<b> Class-conditional image generation samples </b> produced by NAR-XXL on ImageNet 256 × 256.`, "visua": `<div class="column c2i">
        <img src="./static/images/c2i_1.png"
                alt="C2I visualization."/>
      </div>

      <div class="column c2i">
        <img src="./static/images/c2i_2.png"
                alt="C2I visualization."/>
      </div>`},
    "Text to Image": {"intro": ` 512 × 512 <b> text-guided image generation samples </b> produced by LlamaGen-XL-Stage2 with next-token prediction paradigm and
NAR-XL-Stage2 with next-neighbor prediction paradigm. The text prompts are sampled from Parti prompts.`, "visua": `<div class="column t2i">
        <img src="./static/images/t2i.png"
                 alt="T2I visualization."/>
      </div>`},
  }

  let current_choice = $('.current-visualization-tag')[0];
  let current_introduction = $('.current-introduction')[0];
  let current_visualization = $('.current-visualization')[0];

  $('.visualization-tag').on('click', function() {
    let text = $(this)[0].innerText.trim();
    current_choice.innerHTML = `${text} &nbsp`;

    current_introduction.innerHTML = visualization[text]["intro"];
    current_visualization.innerHTML = visualization[text]["visua"];
  });

})
